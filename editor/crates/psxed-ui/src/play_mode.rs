//! Public embedded-play viewport API shared with the frontend.

use egui::Rect;

/// Request emitted by the editor UI for embedded play mode.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum EditorPlaytestRequest {
    /// Cook/build/load and start play mode.
    Play,
    /// Stop play mode and return to edit preview.
    Stop,
    /// Re-cook/rebuild/reload while play mode is already active.
    Rebuild,
    /// The playable viewport was clicked; capture game input there.
    CaptureInput,
}

/// Frontend-owned embedded playtest state, mirrored into the editor
/// for toolbar labels and the playable viewport overlay.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum EditorPlaytestStatus {
    /// No active or recent play mode.
    #[default]
    Idle,
    /// The active project is being cooked.
    Cooking,
    /// `make build-editor-playtest` is running in the frontend.
    Building,
    /// The editor viewport is showing the live in-process emulator.
    Running { input_captured: bool },
    /// The last embedded play attempt failed.
    Failed,
}

impl EditorPlaytestStatus {
    /// True while a Play / Stop pair should be offered.
    pub const fn is_active(self) -> bool {
        matches!(self, Self::Cooking | Self::Building | Self::Running { .. })
    }

    /// True when the 3D viewport should render the live game output.
    pub const fn is_playing(self) -> bool {
        matches!(self, Self::Running { .. })
    }
}

/// What the 3D editor viewport should paint this frame.
#[derive(Clone)]
pub struct EditorViewport3dPresentation {
    /// Egui texture to paint into the 3D viewport.
    pub texture: egui::TextureId,
    /// UV region inside `texture`.
    pub uv: Rect,
    /// Whether this is the editable preview or embedded play output.
    pub mode: EditorViewport3dMode,
}

impl EditorViewport3dPresentation {
    /// Editable 3D preview generated by the editor preview renderer.
    pub fn edit(texture: egui::TextureId) -> Self {
        Self {
            texture,
            uv: Rect::from_min_max(
                egui::pos2(0.0, 0.0),
                egui::pos2(640.0 / 2048.0, 480.0 / 1024.0),
            ),
            mode: EditorViewport3dMode::Edit,
        }
    }

    /// Live game output generated by the frontend emulator.
    pub fn play(texture: egui::TextureId, uv: Rect) -> Self {
        Self {
            texture,
            uv,
            mode: EditorViewport3dMode::Play,
        }
    }
}

/// Mode for the 3D viewport presentation.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum EditorViewport3dMode {
    /// Editable authoring preview.
    Edit,
    /// Embedded emulator framebuffer.
    Play,
}
